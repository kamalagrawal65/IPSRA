				<h1 align="left" class="heading"  >How to Use <i>i</i>BIRA</h1><br />
				<p align="justify" class="content">
				<b><i>i</i>BIRA</b> works on the principle of federated search Engine. 
				It collects information related to bioinformatics domain and creates clusters of information of six categories. 
				These categories are Database, Institutions, Journals, Patents, Software and Web-Server along with countries and Year.		
					
				<br><br>
				For fetching bioinformatics related information use any keywords related to bioinformatics in to the search box aside and press search. You will find a cluster of information at left hand side. When you browse the tree you will able to identify the category of information. If you wish to see a detailed information click on the leaf of the cluster tree and you will notice the information in respective branch.
				<br><br>
